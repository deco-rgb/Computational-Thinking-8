# ###############################################
# ### SETUP ###
import turtle
# ###############################################

t = turtle.Turtle()
t.penup()
t.goto(-100, -100)
t.color("green")
t.pendown()

t.forward(120)
t.left(90)
t.forward(60)
t.left(90)
t.forward(120)
t.left(90)
t.forward(60)
t.left(0)
t.forward(0)



# ###############################################
# ### ENDING ###
turtle.exitonclick()
# ###############################################


